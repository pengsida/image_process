function [ x3, x4, x5, x6 ] = noise( x1, x2, P )
    
    m1=max(abs(x1))/10;                 %????????a1???????????????????
    m2 = max(abs(x2))/10;                 %????????a2???????????????????
    
    [~, N] = size(x1);
    x3 = zeros(100, N);
    x4 = zeros(100, N);
    for n = 1:100                         %?????????????
       x3(n,:) = x1 + normrnd(0,m1,1,P) + j*normrnd(0,m1,1,P);
       x4(n,:) = x2 + normrnd(0,m2,1,P) + j*normrnd(0,m2,1,P);
    end
    
    x5 = zeros(100, N);
    x6 = zeros(100, N);
    for n=1:100                         %?????????????
       x5(n,:) = x1 + normrnd(0,m1,1,P) + j*normrnd(0,m1,1,P);
       x6(n,:) = x2 + normrnd(0,m2,1,P) + j*normrnd(0,m2,1,P);
    end

end

